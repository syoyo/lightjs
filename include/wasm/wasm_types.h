// WASM runtime value (on stack or in variables)
struct WasmValue {
    std::variant<int32_t, int64_t, float, double> data;

    WasmValue() : data(int32_t(0)) {}
    explicit WasmValue(int32_t v) : data(v) {}
    explicit WasmValue(int64_t v) : data(v) {}
    explicit WasmValue(float v) : data(v) {}
    explicit WasmValue(double v) : data(v) {}

    bool isI32() const { return std::holds_alternative<int32_t>(data); }
    bool isI64() const { return std::holds_alternative<int64_t>(data); }
    bool isF32() const { return std::holds_alternative<float>(data); }
    bool isF64() const { return std::holds_alternative<double>(data); }

    int32_t asI32() const { return std::get<int32_t>(data); }
    int64_t asI64() const { return std::get<int64_t>(data); }
    float asF32() const { return std::get<float>(data); }
    double asF64() const { return std::get<double>(data); }
};

// Function signature
struct FuncType {
    std::vector<ValueType> params;
    std::vector<ValueType> results;
};

// Import descriptor
struct Import {
    std::string module;
    std::string name;
    enum class Kind : uint8_t {
        Function = 0,
        Table = 1,
        Memory = 2,
        Global = 3
    } kind;
    uint32_t typeIdx;  // For functions
};

// Export descriptor
struct Export {
    std::string name;
    enum class Kind : uint8_t {
        Function = 0,
        Table = 1,
        Memory = 2,
        Global = 3
    } kind;
    uint32_t idx;
};

// Global variable
struct Global {
    ValueType type;
    bool mutable_;
    WasmValue value;
};

// Memory limits (supports memory64)
struct Limits {
    uint64_t min;
    uint64_t max;
    bool hasMax;
    bool is64;  // memory64 flag
};

// Table element
struct Table {
    ValueType elemType;
    Limits limits;
};

// Instruction
struct Instruction {
    Opcode opcode;
    std::variant<
        int32_t,
        int64_t,
        float,
        double,
        uint32_t,
        std::vector<uint32_t>,
        ValueType,
        std::pair<uint32_t, uint32_t>  // alignment, offset
    > immediate;

    Instruction(Opcode op) : opcode(op), immediate(int32_t(0)) {}
};

// Function code
struct Function {
    uint32_t typeIdx;
    std::vector<ValueType> locals;
    std::vector<Instruction> body;
};

